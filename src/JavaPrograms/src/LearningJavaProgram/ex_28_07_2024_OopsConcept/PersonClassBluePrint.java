package JavaPrograms.src.LearningJavaProgram.ex_28_07_2024_OopsConcept;

public class PersonClassBluePrint {
    // class is like a blueprint it does not use memory
    // it contains attributes/properties and behavior/functionality
    // these are attributes of human
    String name;
    int eyes;
    int nose;

    //these are the behaviour of humans, these are called methods it ois block of code but in oops called methods
    void hear() {
        System.out.println("Human can hear");
    }

    void see() {
        System.out.println("Human can see the world");
    }

    void beathe() {
        System.out.println("Human can breathe to live");
    }

}
